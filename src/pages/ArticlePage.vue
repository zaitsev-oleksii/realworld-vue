<template>
  <div class="article-page" v-if="article">
    <div class="banner">
      <div class="container">
        <h1>{{ article.title }}</h1>

        <div class="article-meta">
          <a href=""><img :src="article.author.image" /></a>
          <div class="info">
            <a href="" class="author">{{ article.author.username }}</a>
            <span class="date">{{ article.createdAt }}</span>
          </div>
          <follow-button
            v-if="following !== null"
            :username="article.author.username"
            :following="following"
            @click="handleFollow"
          />
          &nbsp;&nbsp;
          <button
            class="btn btn-sm btn-outline-primary"
            @click="handleFavorite"
          >
            <i class="ion-heart"></i>
            &nbsp; {{ !favorited ? "Favorite" : "Unfavorite" }} Post
            <span class="counter">({{ article.favoritesCount }})</span>
          </button>
        </div>
      </div>
    </div>

    <div class="container page">
      <div class="row article-content">
        <div class="col-md-12">
          {{ article.body }}
        </div>
      </div>

      <hr />

      <div class="article-actions">
        <div class="article-meta">
          <a href="profile.html"><img :src="article.author.image" /></a>
          <div class="info">
            <a href="" class="author">{{ article.author.username }}</a>
            <span class="date">{{ article.createdAt }}</span>
          </div>

          <follow-button
            v-if="following !== null"
            :username="article.author.username"
            :following="following"
            @click="handleFollow"
          />
          &nbsp;
          <button
            class="btn btn-sm btn-outline-primary"
            @click="handleFavorite"
          >
            <i class="ion-heart"></i>
            &nbsp;
            <span class="counter"
              >{{ !favorited ? "Favorite" : "Unfavorite" }} Post ({{
                article.favoritesCount
              }})</span
            >
          </button>
        </div>
      </div>

      <div class="row">
        <div class="col-xs-12 col-md-8 offset-md-2">
          <comment-form v-if="isAuthorized" />
          <template v-else>
            <span>
              <router-link to="/login">Sign in</router-link> or
              <router-link to="/register">sign up</router-link> to add comments
              on this article.
            </span>
          </template>
          <template v-if="comments.length > 0">
            <comment-card
              v-for="comment in comments"
              :key="comment.id"
              :author="comment.author"
              :createdAt="comment.createdAt"
              :body="comment.body"
            />
          </template>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { ref, onMounted, computed } from "vue";
import { useStore } from "vuex";
import { useRoute, useRouter } from "vue-router";

import articlesAPI from "../api/articles";
import commentsAPI from "../api/comments";

import FollowButton from "../components/FollowButton.vue";
import CommentForm from "../components/CommentForm.vue";
import CommentCard from "../components/CommentCard.vue";

import useFavoriteArticle from "../composables/favorite-article";
import useFollowProfile from "../composables/follow-profile";

import { MISSING_PROFILE_IMAGE_URL } from "../config";

export default {
  name: "ArticlePage",
  components: { FollowButton, CommentForm, CommentCard },
  setup() {
    const store = useStore();
    const router = useRouter();
    const route = useRoute();

    const isAuthorized = computed(() => store.getters.isAuthorized);

    const article = ref(null);

    const comments = ref([]);

    onMounted(async () => {
      const articleData = await articlesAPI.getArticle(route.params.slug);
      if (!articleData) {
        router.push("/");
        return;
      }
      const commentsData = await commentsAPI.getComments(route.params.slug);

      article.value = articleData;
      if (!article.value.author.image) {
        article.value.author.image = MISSING_PROFILE_IMAGE_URL;
      }
      comments.value = commentsData;
    });

    const [following, handleFollow] = useFollowProfile({
      articleSlug: route.params.slug
    });

    const [favorited, handleFavorite] = useFavoriteArticle(route.params.slug);

    return {
      article,
      comments,
      isAuthorized,
      following,
      handleFollow,
      favorited,
      handleFavorite
    };
  }
};
</script>
