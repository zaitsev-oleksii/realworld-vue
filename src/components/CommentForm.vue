<template>
  <form @submit.prevent="handleSubmit" class="card comment-form">
    <div class="card-block">
      <textarea
        class="form-control"
        placeholder="Write a comment..."
        rows="3"
        v-model="commentBody"
      ></textarea>
    </div>
    <div class="card-footer">
      <img
        :src="currentUserImage || MISSING_PROFILE_IMAGE_URL"
        class="comment-author-img"
      />
      <button type="submit" class="btn btn-sm btn-primary">Post Comment</button>
    </div>
  </form>
</template>

<script>
import { ref, computed } from "vue";
import { useStore } from "vuex";

import { MISSING_PROFILE_IMAGE_URL } from "../config";
import commentsAPI from "../api/comments";

export default {
  name: "CommentForm",
  props: {
    articleSlug: String
  },
  emits: {
    "new-comment": null
  },
  setup(props, { emit }) {
    const store = useStore();

    const commentBody = ref("");

    const currentUserImage = computed(() => store.state.user.image);

    const handleSubmit = async () => {
      await commentsAPI.createComment({
        slug: props.articleSlug,
        commentData: { body: commentBody.value },
        token: store.state.user.token
      });
      emit("new-comment");
    };

    return {
      commentBody,
      currentUserImage,
      handleSubmit,
      MISSING_PROFILE_IMAGE_URL
    };
  }
};
</script>
